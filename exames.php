Um tubo flexível e fino (duoendoscópio) é introduzido pela boca passando pelo esôfago e estômago.  No duodeno, se coloca a extremidade distal do aparelho em frente à papila duodenal, que é o local de saída dos ductos biliar e pancreático. Após o exame desta, um cateter ou papilótomo é passado através do canal do endoscópio e introduzido no canal biliar ou pancreático, seguindo-se a injeção de contraste radiológico com acompanhamento em um monitor de radioscopia.</p>
					<p>Podemos realizar biópsias desta região através de pinças ou citologia através de escovas, retirar cálculos dos canais e em casos de estenoses/estreitamentos, colocar próteses.</p>
