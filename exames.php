			<img class="lfloat" src="images/exames/esofagomanometria.jpg" alt="">

			<ul class="exam-nav lfloat">
				<li>
					<a href="#">O que é o exame</a>
					<aside><p>A Esofagomanometria é um exame que permite avaliar a motilidade do esôfago e de suas extremidades, que tem o nome de esfíncter.</p></aside>
				</li>
				<li>
					<a href="#">Como é feito</a>
					<aside><p>Inicialmente é feita anestesia de uma das narinas com lidocaína gel (anestesia local). Não é feita sedação.</p>
					<p>Posteriormente é introduzida na narina anestesiada uma sonda de fino calibre (4mm).</p>
					<p>Em seguida serão fornecidos pequenos goles de água que deverão ser deglutidos pelo paciente enquanto a sonda registra a atividade motora do esôfago.
					O exame terá duração de aproximadamente 30 minutos.</p></aside>
				</li>
				<li>
					<a href="#">Antes e depois do exame</a>
					<aside><p><strong>ANTES DO EXAME:</strong></p>
					<p>É necessário apenas o jejum de quatro horas.</p>
					<p>A última refeição antes do exame deverá ser de líquidos.</p>
					<p><strong>Suspender 24h antes do exame, caso em uso, as seguintes medicações:</strong></p>
					<p>» Nitratos (Monocordil, Isordil)<br>
					» Bloqueadores dos canais de cálcio (Adalat, Nifedipina, Balcor, Diltiazem, Verapamil, Dilacoron)<br>
					» Pró-cinéticos (Bromoprida, Plamet, Digesan, Plasil, Metoclopramida)<br>
					» Ansiolíticos (Diazepam, Valium, Lexotan, Olcadil, Lorax)</p>

					<p><strong>Lembrar de levar:</strong></p>
					<p>» Pedido do exame<br>
					» Se for o caso, carteira do plano de saúde e eventual autorização prévia – informar-se previamente com a recepcionista da Endoview<br>
					» Exames anteriores como Esofagografia (RX), Endoscopia digestiva, Esofagomanometria, pHmetria de 24h</p>
					  
					<p><strong>APÓS O EXAME:</strong></p>
					<p>Após o exame o paciente poderá retornar às suas atividades normais, inclusive de trabalho.</p>
					<p>Alimentação: pode alimentar-se normalmente após o exame.</p>
					<p><strong>Sensação na garganta:</strong></p>
					<p>» Imediatamente após o exame permanece leve sensação de anestesia na garganta (como se estivesse “grossa”), desaparecendo após alguns minutos.</p>
					<p>» Embora seja incomum, após a cessação da anestesia da garganta pode surgir, eventualmente, leve sensação de irritação que costuma desaparecer completamente após algumas horas ou poucos dias.</p></aside>
